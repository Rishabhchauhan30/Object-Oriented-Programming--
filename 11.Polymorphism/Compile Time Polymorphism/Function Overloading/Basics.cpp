/*
    * Function Overloading
        Function Overloading is the process having two or more functions with the same name But
        Different in Parameters is called as Function Overloading
    In Function Overloading function is redefined by using either types of arguments or number of arguments.
    It is only through this the compiler can differentiate btw functions

    * Advantage
        Advantage of using Function overloading is it increase readability of the program bcz we dont need to use different names for 
        same action.

*/